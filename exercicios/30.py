pessoas = 20
gatos = 30
cachoros = 15

if pessoas < gatos:
    print("Muitos gatos! O mundo está condenado")

if pessoas > gatos:
    print("Poucos gatos! O mundo está salvo.")

if pessoas < cachoros:
    print("O mundo está todo babado.")

if pessoas > cachoros:
    print("O mundo está seco.")

cachoros += 5

if pessoas >= cachoros:
    print("Pessoas é maior ou igual a cachoros")

if pessoas <= cachoros:
    print("Pessoas é menor ou igual a cachoros")

if pessoas == cachoros:
    print("Pessoas é igual a cachoros")
